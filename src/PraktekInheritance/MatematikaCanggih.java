package PraktekInheritance;

public class MatematikaCanggih {
    void modulus (int a, int b){
        int hasil = a % b;
        System.out.println(a + " % " + b + ": " + hasil);
    }
    void modulus (double a, double b, double c){
        double hasil = a % b % c;
        System.out.println(a + " % " + b + " % " + c + ": " + hasil);
    }
}
