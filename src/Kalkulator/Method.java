package Kalkulator;

public class Method {
    void penjumlahan (int bilangan,int bil){
        int hasil = bilangan + bil ;
        System.out.println("Pertambahan: " + bilangan +" + " + bil + " = " + hasil);
    }
    void pengurangan (int bilangan,int bil){
        int hasil = bilangan - bil ;
        System.out.println("Pengurangan: " + bilangan +" + " + bil + " = " + hasil);
    }
    void perkalian (int bilangan,int bil){
        int hasil = bilangan * bil ;
        System.out.println("Perkalian: " + bilangan +" + " + bil + " = " + hasil);
    }
    void pembagian (int bilangan,int bil){
        int hasil = bilangan / bil ;
        System.out.println("Pembagian: " + bilangan +" + " + bil + " = " + hasil);
    }


}
